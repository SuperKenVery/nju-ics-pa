void set_CF_add(u32 a, u32 b, usize size){
	u32 r=cut(a+b,size);
	printf("Calculating CF: a=%p, b=%p, result=%p, size=%u\n",(void*)a,(void*)b,(void*)r,size);
	if(r<a){
		cpu.eflags.CF=1;
	}else{
		cpu.eflags.CF=0;
	}
	printf("CF is %u\n",cpu.eflags.CF);
}

void set_PF(u32 x){
	u8 count=0;
	for(int i=0;i<8;i++){
		u8 bit=x&1;
		if(bit) count++;
		x=x>>1;
	}

	if(count%2==1){
		cpu.eflags.PF=1;
	}else{
		cpu.eflags.PF=0;
	}
}


